let mock_server_generator_dep = "gen/gen_server.exe"
let gen_server_metainfo = "gen/gen.exe"

module Dparts = Dune_file_parts
include Dparts.Fcomb.Ops

module Dcomb = struct
  [@@@warning "-32"]

  include Dparts.Fcomb
  include Dparts
end

let () =
  let outf fmt = printf fmt in
  let output_fmtd_newline fmtd = outf "%a@." fmtd () in
  let target = ArgOptions.(get_option_exn (StringOption "-target")) in
  let output_duneinc_header target_name =
     outf ";; generated by %s@\n" __FILE__;
     outf ";; file: %s@\n" target_name;
  in
  match String.chop_suffix ".gen" target with
  | Some ("apidir-typescript-tests-dune.inc" as target_name) ->
    output_duneinc_header target_name;
    output_fmtd_newline & Dcomb.(
      aggregate [
        Rule.mk_gen [
          target' (fmt "%s_mock_server.ml");
          Deps.(mk [
            named "mock_server_gen" (atomic mock_server_generator_dep);
          ]);
          Action.(mk &
            with_stdout_to (`expr (fmt "%s_mock_server.ml")) (
              run "%{mock_server_gen}" [ fmt "%s_mock_server.ml" ]
            ));
        ]
        |> many;
        Executable.mk ~name:(fmt "%s_mock_server")
          ~modules':[ fmt "%s_mock_server" ]
          ~libraries:[ "gen_mock_server" ]
          ~modes:[ "js" ]
          ~preprocess:[ "(pps js_of_ocaml-ppx)" ]
          [ ]
        |> many;
        Rule.mkp_gen [
          target' (fmt "%s_server.js");
          Deps.(mk [
            fmt "%s_mock_server.bc.js"
          ]);
          Action.(mk &
            copy (str "%{deps}") (str "%{target}"));
        ]
        |> many;
        Rule.mkp_gen [
          target' (fmt "%s.ts");
          Deps.(mk [
            named "metainfo_gen" (atomic gen_server_metainfo);
          ]);
          Action.(mk &
            with_stdout_to `target &
              run "%{metainfo_gen}" &. str "%{target}")
        ] |> many;
      ]
      |> const & Bindoj_test_common_apidir_examples.All.all |&> fst
    )
  | _ -> eprintf "invalid -target value %S" target; exit 2
